# 图片调整脚本
# 需要先安装 ImageMagick: https://imagemagick.org/script/download.php

# 或者使用在线工具：
# 1. 访问 https://www.iloveimg.com/resize-image
# 2. 上传 android-chrome-192x192.png
# 3. 调整为 48x48，下载为 icon48.png
# 4. 再次上传，调整为 128x128，下载为 icon128.png

Write-Host "请使用以下在线工具调整图片："
Write-Host "1. 访问: https://www.iloveimg.com/resize-image"
Write-Host "2. 上传 android-chrome-192x192.png"
Write-Host "3. 调整为 48x48 像素，下载为 icon48.png"
Write-Host "4. 再次调整为 128x128 像素，下载为 icon128.png"
Write-Host ""
Write-Host "完成后，将文件放到 icons/ 文件夹"
